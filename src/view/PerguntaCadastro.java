    private Pergunta capturarPergunta() {
        String pergunta = campoPergunta.getText();
        String opcaoA = campoA.getText();
        String opcaoB = campoB.getText();
        String opcaoC = campoC.getText();
        String opcaoD = campoD.getText();
        String opcaoCorreta = campoOpcaoCorreta.getText();
        
        Pergunta p = new Pergunta(pergunta, opcaoA, opcaoB, opcaoC, opcaoD, opcaoCorreta);
        return p;
    }

    private void preencherFormulario(Pergunta pergunta) {
        campoPergunta.setText(pergunta.getPergunta());
        campoA.setText(pergunta.getA());
        campoB.setText(pergunta.getB());
        campoC.setText(pergunta.getC());
        campoD.setText(pergunta.getD());
        campoOpcaoCorreta.setText(pergunta.getOpcaoCorreta());
    }
