    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        Pergunta p = capturarPergunta();
        if (this.pergunta == null) {
            controller.salvarPergunta(p);
        }else{
            p.setId(this.pergunta.getId());
            controller.atualizarPergunta(p);
        }
            
        
        mostrarListagem();
    }//GEN-LAST:event_botaoCadastrarActionPerformed

    private void botaoCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCancelarActionPerformed
        this.pergunta = null;
        mostrarListagem();
    }//GEN-LAST:event_botaoCancelarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        controller.excluir(this.pergunta.getId());
        mostrarListagem();
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void mostrarListagem(){
        PerguntaListagem p = new PerguntaListagem();
        p.setTitle("Listagem de Perguntas");
        p.setLocationRelativeTo(null);
        this.setVisible(false);
        p.setVisible(true);
    }
    
