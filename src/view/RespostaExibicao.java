    private void BotaoProximaPerguntaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoProximaPerguntaActionPerformed
        String resposta = CampoResposta.getText();
        if (pg == 9) {
            RespostaResultado rr = new RespostaResultado();
            rr.setTitle("Resultado");
            rr.setLocationRelativeTo(null);
            this.setVisible(false);
            rr.setVisible(true);
            double Erro = 10 - Acertos;
            double Acertos1 = (Acertos / 10) * 100;
            String Acerto = Double.toString(Acertos1);
            rr.Porcentagem.setText(Acerto + "%");
            rr.acertoserro.setText("VocÃª acertou " + Acertos + " e errou " + Erro);

        } else if ("a".equals(CampoResposta.getText()) || "b".equals(CampoResposta.getText()) || "c".equals(CampoResposta.getText()) || "d".equals(CampoResposta.getText())) {
            pg = pg + 1;
            Pergunta(pg);
            if (OpcaoCorreta.equals(resposta)) {
                Acertos = Acertos + 1;
            }
        }


    }//GEN-LAST:event_BotaoProximaPerguntaActionPerformed

