        public Pergunta Pergunta(int pg) {
        ArrayList<Pergunta> listaPerguntas = controller.RandomizarPergunta();
        Pergunta p = listaPerguntas.get(pg);
        CampoPergunta.setText(p.getPergunta());
        CampoRespostaA.setText(p.getA());
        CampoRespostaB.setText(p.getB());
        CampoRespostaC.setText(p.getC());
        CampoRespostaD.setText(p.getD());
        OpcaoCorreta = p.getOpcaoCorreta();
        return p;
    }

